package id.ac.its.mirzaq065.Project1;

public class Triangle {
	private double alas;
	private double tinggi;
	
	public Triangle() {
		this.alas = 0;
		this.tinggi = 0;
	}
	public Triangle(double alas, double tinggi) {
		this.alas = alas;
		this.tinggi = tinggi;
	}
	public double getAlas() {
		return alas;
	}
	public void setAlas(double alas) {
		this.alas = alas;
	}
	public double getTinggi() {
		return tinggi;
	}
	public void setTinggi(double tinggi) {
		this.tinggi = tinggi;
	}
	public double getMiring() {
		return Math.sqrt(alas*alas+tinggi*tinggi);
	}
	public double getArea() {
		return alas*tinggi;
	}
	
	public double getKeliling() {
		return alas+tinggi+getMiring();
	}
}
